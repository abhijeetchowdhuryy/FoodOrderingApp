package com.example.sizzlingbitesapp;

public class StaticRVModel2 {
    private int image;
    private int pos;

    public StaticRVModel2(int image, int pos) {
        this.image = image;
        this.pos = pos;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public int getPos() {
        return pos;
    }
}
